        if isinstance(context, SingletonObjectContext):
            obj = context.singleton
        else:
            raise IDLError(
                "Expected context for non-function : {}".format(str(context))
            )

        if isinstance(obj, Function):
            raise IDLError(
                "Expected non-function object, got {}".format(
                    context.obj
                )
            )

